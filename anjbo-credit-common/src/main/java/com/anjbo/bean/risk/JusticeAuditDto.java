package com.anjbo.bean.risk;

import java.util.Date;

import com.anjbo.bean.BaseDto;
/**
 * 法务审批
 * @author Administrator
 *
 */
public class JusticeAuditDto extends BaseDto{

	private Integer id;

    /**
     *订单编号
     *
     * This field corresponds to the database column tbl_risk_audit_officer.orderNo
     *
     * @mbggenerated 2017-06-08
     */
    private String orderNo;

    /**
     *审批时间
     *
     * This field corresponds to the database column tbl_risk_audit_officer.auditTime
     *
     * @mbggenerated 2017-06-08
     */
    private Date auditTime;

    /**
     *0:不展示1:展示
     *
     * This field corresponds to the database column tbl_risk_audit_officer.isShow
     *
     * @mbggenerated 2017-06-08
     */
    private Integer isShow;

    /**
     *备注（如不通过原因或者其他）
     *
     * This field corresponds to the database column tbl_risk_audit_officer.remark
     *
     * @mbggenerated 2017-06-08
     */
    private String remark;
    /**
     * 审批状态 1:通过,2不通过(退回)
     */
    private int auditStatus;
    /**
     * 审批人uid
     */
    private String handleUid;
    /**
     * 处理人名称
     */
    private String handleName;
    /**
     * 下一节点处理人
     */
    private String nextHandleUid;

    public int getAuditStatus() {
		return auditStatus;
	}

	public void setAuditStatus(int auditStatus) {
		this.auditStatus = auditStatus;
	}

	public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getOrderNo() {
        return orderNo;
    }

    public void setOrderNo(String orderNo) {
        this.orderNo = orderNo;
    }

    public Date getAuditTime() {
        return auditTime;
    }

    public void setAuditTime(Date auditTime) {
        this.auditTime = auditTime;
    }

    public Integer getIsShow() {
        return isShow;
    }

    public void setIsShow(Integer isShow) {
        this.isShow = isShow;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

	public String getHandleUid() {
		return handleUid;
	}

	public void setHandleUid(String handleUid) {
		this.handleUid = handleUid;
	}

	public String getHandleName() {
		return handleName;
	}

	public void setHandleName(String handleName) {
		this.handleName = handleName;
	}

	public String getNextHandleUid() {
		return nextHandleUid;
	}

	public void setNextHandleUid(String nextHandleUid) {
		this.nextHandleUid = nextHandleUid;
	}
}