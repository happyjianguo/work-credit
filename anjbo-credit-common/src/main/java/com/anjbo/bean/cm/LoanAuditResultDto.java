package com.anjbo.bean.cm;

import java.util.Date;

public class LoanAuditResultDto{
	private int id;  //审批结果反馈表
	private String orderNo;  //订单编号
	private String result;  //审批是否通过（Y通过 N不通过）
	private String reason;  //备注
	private double loanAmount;  //贷款金额
	private String loanLimit;  //贷款期限
	private Date createTime;  //审批时间
	private char feedType;  //意见类型（0-意见反馈1-补充申请信息2-补充影像资料3-补充申请信息和影像资料4-贷款拒绝通知5-贷款删除通知6-贷款退回通知）
	private String feedSign;  //签署意见（用于反馈意见给合作伙伴，主要用于要求补充贷款信息、影像资料等）
	private Date feedTime;//意见反馈时间
	private String appNo;
	
	
	public String getAppNo() {
		return appNo;
	}
	public void setAppNo(String appNo) {
		this.appNo = appNo;
	}
	public Date getFeedTime() {
		return feedTime;
	}
	public void setFeedTime(Date feedTime) {
		this.feedTime = feedTime;
	}
	public char getFeedType() {
		return feedType;
	}
	public void setFeedType(char feedType) {
		this.feedType = feedType;
	}
	public String getFeedSign() {
		return feedSign;
	}
	public void setFeedSign(String feedSign) {
		this.feedSign = feedSign;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public double getLoanAmount() {
		return loanAmount;
	}
	public void setLoanAmount(double loanAmount) {
		this.loanAmount = loanAmount;
	}
	public String getLoanLimit() {
		return loanLimit;
	}
	public void setLoanLimit(String loanLimit) {
		this.loanLimit = loanLimit;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	
}