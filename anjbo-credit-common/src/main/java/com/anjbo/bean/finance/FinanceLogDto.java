package com.anjbo.bean.finance;

import com.anjbo.bean.BaseDto;

public class FinanceLogDto extends BaseDto{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String beanColumn;   //实体类属性
	private String orderNo;
	private String colName;   //字段名称
	private String startVal;  //初始值
	private String endVal;  //结束值
	private int isShow;  //0:不展示1：展示
	private int type;  //1:申请放款修改，2：收利息修改  3扣回后置费用修改
	
	public String getBeanColumn() {
		return beanColumn;
	}
	public void setBeanColumn(String beanColumn) {
		this.beanColumn = beanColumn;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public String getColName() {
		return colName;
	}
	public void setColName(String colName) {
		this.colName = colName;
	}
	public String getStartVal() {
		return startVal;
	}
	public void setStartVal(String startVal) {
		this.startVal = startVal;
	}
	public String getEndVal() {
		return endVal;
	}
	public void setEndVal(String endVal) {
		this.endVal = endVal;
	}
	public int getIsShow() {
		return isShow;
	}
	public void setIsShow(int isShow) {
		this.isShow = isShow;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	
	

}
