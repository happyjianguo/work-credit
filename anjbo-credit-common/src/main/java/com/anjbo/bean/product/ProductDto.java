package com.anjbo.bean.product;

import java.util.List;

import com.anjbo.bean.BaseDto;

/**
 * 业务产品
 * @author Object
 *
 */
public class ProductDto extends BaseDto{
	
	private static final long serialVersionUID = 1L;

	/** 业务产品表  */
	private int id;

	/** 产品代号 */
	private String productCode;  
	
	/** 产品名称  */
	private String productName;

	/** 产品描述  */
	private String productDescribe;
	
	/** 城市Code **/
	private String cityCode;
	
	/** 城市名称 **/
	private String cityName;
	
	/** 产品类型 **/
	private int type;

	/** 产品流程 **/
	private List<ProductProcessDto> productProcessDtos;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getProductCode() {
		return productCode;
	}

	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getProductDescribe() {
		return productDescribe;
	}

	public void setProductDescribe(String productDescribe) {
		this.productDescribe = productDescribe;
	}

	public String getCityCode() {
		return cityCode;
	}

	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}

	public List<ProductProcessDto> getProductProcessDtos() {
		return productProcessDtos;
	}

	public void setProductProcessDtos(List<ProductProcessDto> productProcessDtos) {
		this.productProcessDtos = productProcessDtos;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getCityName() {
		return cityName;
	}

	public void setCityName(String cityName) {
		this.cityName = cityName;
	}

}
