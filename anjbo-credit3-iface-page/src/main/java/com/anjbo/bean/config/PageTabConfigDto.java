package com.anjbo.bean.config;

import java.util.List;

import com.anjbo.bean.BaseDto;

/**
 * 表单标签
 * @author lic
 * @date 2017-8-17
 */
public class PageTabConfigDto extends BaseDto{

	private static final long serialVersionUID = 1L;

	private int id;
	
	/** 标签标题 **/
	private String title;
	
	/** 保存按钮名称 **/
	private String saveButName;
	
	/** 保存按钮Url **/
	private String saveButUrl;
	
	/** 保存按钮后面的备注 **/
	private String remarks;
	
	/** 获取数据url **/
	private String dataUrl;
	
	/** 标签数据插入的表名 **/
	private String tblName;
	
	/** 表格区域 **/
	private List<PageTabRegionConfigDto> pageTabRegionConfigDtos;
	
	/** 排序 **/
	private int sort;
	
	/** 标签归类 **/
	private String tabClass;
	
	/** 标签包含那些区域（用逗号分隔） **/
	private String regionClasses;
	
	/** 是否必填(1,非必填，2,必填) **/
	private int isNeed;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSaveButName() {
		return saveButName;
	}

	public void setSaveButName(String saveButName) {
		this.saveButName = saveButName;
	}

	public String getSaveButUrl() {
		return saveButUrl;
	}

	public void setSaveButUrl(String saveButUrl) {
		this.saveButUrl = saveButUrl;
	}

	public List<PageTabRegionConfigDto> getPageTabRegionConfigDtos() {
		return pageTabRegionConfigDtos;
	}

	public void setPageTabRegionConfigDtos(
			List<PageTabRegionConfigDto> pageTabRegionConfigDtos) {
		this.pageTabRegionConfigDtos = pageTabRegionConfigDtos;
	}

	public int getSort() {
		return sort;
	}

	public void setSort(int sort) {
		this.sort = sort;
	}

	public String getTabClass() {
		return tabClass;
	}

	public void setTabClass(String tabClass) {
		this.tabClass = tabClass;
	}

	public String getRegionClasses() {
		return regionClasses;
	}

	public void setRegionClasses(String regionClasses) {
		this.regionClasses = regionClasses;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public String getDataUrl() {
		return dataUrl;
	}

	public void setDataUrl(String dataUrl) {
		this.dataUrl = dataUrl;
	}

	public String getTblName() {
		return tblName;
	}

	public void setTblName(String tblName) {
		this.tblName = tblName;
	}

	public int getIsNeed() {
		return isNeed;
	}

	public void setIsNeed(int isNeed) {
		this.isNeed = isNeed;
	}
	
}
