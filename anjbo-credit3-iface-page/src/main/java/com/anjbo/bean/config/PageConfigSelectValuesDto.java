package com.anjbo.bean.config;

import com.anjbo.bean.BaseDto;

/**
 * 配置下拉框选项值
 * @author lic
 * @date 2017-8-19
 */
public class PageConfigSelectValuesDto extends BaseDto{

	private static final long serialVersionUID = 1L;

	private int id;
	
	/** 归类 **/
	private String selectTypeClass;
	
	/** 数据来源(1,固定数据 2,每次请求接口) **/
	private int type;
	
	/** 请求接口url，有参数拼接在后面 **/
	private String url;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getSelectTypeClass() {
		return selectTypeClass;
	}

	public void setSelectTypeClass(String selectTypeClass) {
		this.selectTypeClass = selectTypeClass;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	
}
