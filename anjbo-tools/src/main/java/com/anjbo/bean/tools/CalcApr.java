package com.anjbo.bean.tools;

import com.anjbo.bean.BaseDto;

/**
 * 年化利率计算器
 * @author jiangyq
 *
 * @date 2017年11月13日 上午11:21:49
 */
public class CalcApr extends BaseDto{
	
	private int id;
	
	/** 贷款总额 */
	private double loanAmount;
	/** 贷款年利率 */
	private double loanApr;
	/** 贷款期限（分期数） */
	private int period;
	/** 其他费用 */
	private double otherFee;
	
	/** 是否回存 */
	private int isBack;
	/** 回存总额 */
	private double backAmount;
	/** 回存利率 */
	private double backApr;
	/** 回存期限 */
	private int backPeriod;
	
	/** 单期手续费（%） */
	private double singleFee;
	
	/** 计算类型1：银行贷款，2：固定费率 */
	private int type;
	/** 年利率（计算结果） */
	private double apr;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getLoanAmount() {
		return loanAmount;
	}
	public void setLoanAmount(double loanAmount) {
		this.loanAmount = loanAmount;
	}
	public double getLoanApr() {
		return loanApr;
	}
	public void setLoanApr(double loanApr) {
		this.loanApr = loanApr;
	}
	public int getPeriod() {
		return period;
	}
	public void setPeriod(int period) {
		this.period = period;
	}
	public double getOtherFee() {
		return otherFee;
	}
	public void setOtherFee(double otherFee) {
		this.otherFee = otherFee;
	}
	public int getIsBack() {
		return isBack;
	}
	public void setIsBack(int isBack) {
		this.isBack = isBack;
	}
	public double getBackAmount() {
		return backAmount;
	}
	public void setBackAmount(double backAmount) {
		this.backAmount = backAmount;
	}
	public double getBackApr() {
		return backApr;
	}
	public void setBackApr(double backApr) {
		this.backApr = backApr;
	}
	public int getBackPeriod() {
		return backPeriod;
	}
	public void setBackPeriod(int backPeriod) {
		this.backPeriod = backPeriod;
	}
	public double getSingleFee() {
		return singleFee;
	}
	public void setSingleFee(double singleFee) {
		this.singleFee = singleFee;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public double getApr() {
		return apr;
	}
	public void setApr(double apr) {
		this.apr = apr;
	}
	
}
