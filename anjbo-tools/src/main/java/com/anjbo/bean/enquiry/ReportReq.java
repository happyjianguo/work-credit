package com.anjbo.bean.enquiry;

/**
 * 轮询询价接口请求bean
 * 
 * @author zhuzj
 * 
 */
public class ReportReq {
	private String type;// 1：单条询价结果；2：所有新的询价结果；3：按日期范围返回结果
	private String pgsqId;// 流水号

	public ReportReq(String type, String pgsqId) {
		this.type = type;
		this.pgsqId = pgsqId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getPgsqId() {
		return pgsqId;
	}

	public void setPgsqId(String pgsqId) {
		this.pgsqId = pgsqId;
	}

}
