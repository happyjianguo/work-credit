package com.anjbo.bean.system;

public class AmsDto {
	private String phone;
	private String ip;
	private String svrIp; 
	private String smsContent; 
	private String smsComeFrom;
	
	private String amsCode;
	
	public String getAmsCode() {
		return amsCode;
	}
	public void setAmsCode(String amsCode) {
		this.amsCode = amsCode;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getSvrIp() {
		return svrIp;
	}
	public void setSvrIp(String svrIp) {
		this.svrIp = svrIp;
	}
	public String getSmsContent() {
		return smsContent;
	}
	public void setSmsContent(String smsContent) {
		this.smsContent = smsContent;
	}
	public String getSmsComeFrom() {
		return smsComeFrom;
	}
	public void setSmsComeFrom(String smsComeFrom) {
		this.smsComeFrom = smsComeFrom;
	}
	
}
